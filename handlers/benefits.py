"""
Обработчики для раздела льготы Московского Политеха
"""

from aiogram import Router, types, F
from aiogram.filters import StateFilter
from aiogram.types import ReplyKeyboardMarkup, KeyboardButton
from aiogram.fsm.context import FSMContext
from aiogram.fsm.state import State, StatesGroup

from handlers.navigation import get_main_menu_keyboard
from utils.logger import logger

router = Router()


class BenefitsForm(StatesGroup):
    """Состояния для раздела льготы"""
    viewing_benefits = State()


def get_benefits_keyboard():
    """Клавиатура раздела льготы"""
    return ReplyKeyboardMarkup(
        keyboard=[
            [KeyboardButton(text="💰 Стипендия"), KeyboardButton(text="🤝 Материальная помощь")],
            [KeyboardButton(text="🏛️ Материальная поддержка (Мэрия)"), KeyboardButton(text="🚇 Льготный проезд")],
            [KeyboardButton(text="📋 Все льготы"), KeyboardButton(text="📝 Как оформить")],
            [KeyboardButton(text="◀️ Назад в главное меню")],
        ],
        resize_keyboard=True
    )


@router.message(F.text == "📗 Льготы студентов")
async def show_student_menu(message: types.Message, state: FSMContext):
    """Показать меню льгот студента"""
    logger.info(f"✅ Обработчик льгот вызван для пользователя {message.from_user.id}")
    
    await state.set_state(BenefitsForm.viewing_benefits)
    logger.info(f"📌 Состояние установлено: BenefitsForm.viewing_benefits")
    
    student_text = """
📗 **Льготы для студентов**

Здесь ты найдёшь информацию о:
• Стипендиях и выплатах
• Материальной помощи
• Льготном проезде
• Других социальных льготах

Выбери интересующий раздел:
"""
    
    await message.answer(
        student_text,
        reply_markup=get_benefits_keyboard(),
        parse_mode="Markdown"
    )


